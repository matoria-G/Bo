package com.bo.bean;

public class StageStatusBean {

	private String entity;
	private String srcTable;
	private String srcKey;
	private String excpCode;
	private String excpId;
	private String stageID;
	private String stageStaus;

	
	public String getEntity() {
		return entity;
	}
	public void setEntity(String entity) {
		this.entity = entity;
	}
	public String getSrcTable() {
		return srcTable;
	}
	public void setSrcTable(String srcTable) {
		this.srcTable = srcTable;
	}
	public String getSrcKey() {
		return srcKey;
	}
	public void setSrcKey(String srcKey) {
		this.srcKey = srcKey;
	}
	public String getExcpCode() {
		return excpCode;
	}
	public void setExcpCode(String excpCode) {
		this.excpCode = excpCode;
	}
	public String getExcpId() {
		return excpId;
	}
	public void setExcpId(String excpId) {
		this.excpId = excpId;
	}
	public String getStageID() {
		return stageID;
	}
	public void setStageID(String stageID) {
		this.stageID = stageID;
	}
	public String getStageStaus() {
		return stageStaus;
	}
	public void setStageStaus(String stageStaus) {
		this.stageStaus = stageStaus;
	}

}
